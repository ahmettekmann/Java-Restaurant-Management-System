    private void btn_faturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_faturaActionPerformed
        if (lbl_geneltoplam.getText().length() > 0) {
            txtarea_fatura.setText("");
            DateFormat sdf = new SimpleDateFormat("dd/MM/YYYY HH:mm:ss");
            Date tarih = new Date();
            txtarea_fatura.append("Restaurant Yönetim Sistemi: \n\n"
                    + sdf.format(tarih)
                    + "\n\nİçecek Fiyatı: \t" + lbl_icecek.getText()
                    + "\nYemek Fiyat: \t" + lbl_yemek.getText()
                    + "\nAra Toplam: \t" + lbl_aratoplam.getText()
                    + "\nGenel Toplam: \t" + lbl_geneltoplam.getText()
            );

            try {
                conn = db.java_db();
                String sql = "INSERT INTO Islemler (icecekfiyat,yemekfiyat,bahsis,kdv,aratoplam,toplam,tarih) "
                        + "VALUES(?,?,?,?,?,?,?)";
                pst = conn.prepareStatement(sql);
                pst.setString(1, String.valueOf(fiyatlar[0]));
                pst.setString(2, String.valueOf(fiyatlar[1]));
                pst.setString(3, String.valueOf(fiyatlar[2]));
                pst.setString(4, String.valueOf(fiyatlar[3]));
                pst.setString(5, String.valueOf(fiyatlar[4]));
                pst.setString(6, String.valueOf(fiyatlar[5]));
                pst.setString(7, sdf.format(tarih));

                pst.execute(); // INSERT, UPDATE ve DELETE sorguları için
                // rs=pst.executequery(); SELECT sorgusunda kullanılır
                JOptionPane.showMessageDialog(null, "Kayıt başarıyla eklenmiştir.");
            } catch (Exception ex) {

            } finally {
                try {
                    conn.close();
                } catch (Exception ex) {

                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "Lütfen genel toplam hesabını yapınız'");
        }
    }//GEN-LAST:event_btn_faturaActionPerformed

