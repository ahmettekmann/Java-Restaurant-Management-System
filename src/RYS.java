    void yiyecekhesapla(javax.swing.JTextField txt, javax.swing.JCheckBox ck) {
        try {
            int adet = Integer.parseInt(txt.getText());
            conn = db.java_db();
            String sql = "SELECT * FROM Yemekler WHERE yemekad='" + ck.getText() + "'";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            double fiyat = Double.parseDouble(rs.getString("yemekfiyat"));
            if (ck.isSelected()) {
                fiyatlar[1] = fiyatlar[1] + fiyat * adet;
                lbl_yemek.setText(String.format("%.2f TL", fiyatlar[1]));
                txt.setEnabled(false);
            } else {
                fiyatlar[1] = fiyatlar[1] - fiyat * adet;
                lbl_yemek.setText(String.format("%.2f TL", fiyatlar[1]));
                txt.setEnabled(true);
            }
        } catch (Exception ex) {

        } finally {
            try {
                conn.close();
            } catch (Exception ex) {

            }
        }
    }

